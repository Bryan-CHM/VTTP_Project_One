package VTTP.Project.VTTP_Project_One.models;

public class Animal {
    private String name;
    private String animal_type;
    private String active_time;
    private Double min_length;
    private Double max_length;
    private Double min_weight;
    private Double max_weight;
    private Integer lifespan;
    private String habitat;
    private String diet;
    private String location;
    private String image_url;
    private Integer likes;
    
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getAnimal_type() {
        return animal_type;
    }
    public void setAnimal_type(String animal_type) {
        this.animal_type = animal_type;
    }
    public String getActive_time() {
        return active_time;
    }
    public void setActive_time(String active_time) {
        this.active_time = active_time;
    }
    public Double getMin_length() {
        return min_length;
    }
    public void setMin_length(Double min_length) {
        this.min_length = min_length;
    }
    public Double getMax_length() {
        return max_length;
    }
    public void setMax_length(Double max_length) {
        this.max_length = max_length;
    }
    public Double getMin_weight() {
        return min_weight;
    }
    public void setMin_weight(Double min_weight) {
        this.min_weight = min_weight;
    }
    public Double getMax_weight() {
        return max_weight;
    }
    public void setMax_weight(Double max_weight) {
        this.max_weight = max_weight;
    }
    public Integer getLifespan() {
        return lifespan;
    }
    public void setLifespan(Integer lifespan) {
        this.lifespan = lifespan;
    }
    public String getHabitat() {
        return habitat;
    }
    public void setHabitat(String habitat) {
        this.habitat = habitat;
    }
    public String getDiet() {
        return diet;
    }
    public void setDiet(String diet) {
        this.diet = diet;
    }
    public String getLocation() {
        return location;
    }
    public void setLocation(String location) {
        this.location = location;
    }
    public String getImage_url() {
        return image_url;
    }
    public void setImage_url(String image_url) {
        this.image_url = image_url;
    }
    public Integer getLikes() {
        return likes;
    }
    public void setLikes(Integer likes) {
        this.likes = likes;
    }
   
}
